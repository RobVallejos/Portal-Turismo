function filtrarAvisos() {

   var produc = [];
   var comerciosParaElmapa = [];
   var cont = 0;


   comercio.forEach((comercios) => {

      console.log(comercios)
      cont = cont + 1;

      console.log(cont)


      comercios.avisos.forEach((aviso) => {

         switch (true) {
            case filtroArticuloRegionalIsChecked && filtroServicioIsChecked && filtroOtrosIsChecked:
               if (aviso.articuloRegional || aviso.servicio || aviso.otros) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;

            case filtroArticuloRegionalIsChecked && filtroServicioIsChecked && !filtroOtrosIsChecked:
               if (aviso.articuloRegional || aviso.servicio) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;

            case filtroArticuloRegionalIsChecked && !filtroServicioIsChecked && filtroOtrosIsChecked:
               if (aviso.articuloRegional || aviso.otros) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;

            case filtroArticuloRegionalIsChecked && !filtroServicioIsChecked && !filtroOtrosIsChecked:
               if (aviso.articuloRegional) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;

            case !filtroArticuloRegionalIsChecked && filtroServicioIsChecked && filtroOtrosIsChecked:
               if (aviso.servicio || aviso.otros) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;

            case !filtroArticuloRegionalIsChecked && filtroServicioIsChecked && !filtroOtrosIsChecked:
               if (aviso.servicio) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;

            case !filtroArticuloRegionalIsChecked && !filtroServicioIsChecked && filtroOtrosIsChecked:
               if (aviso.otros) {
                  produc.push(aviso);
                  if (!comerciosParaElmapa.includes(comercios)) {
                     comerciosParaElmapa.push(comercios);
                 }
               }
               break;
         }

      })

   })

   llenarMapa(comerciosParaElmapa);
   añadirAvisosAlDOM(produc);
}


